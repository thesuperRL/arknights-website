function getNested(obj: Record<string, unknown>, path: string): unknown {
  const parts = path.split('.');
  let current: unknown = obj;
  for (const p of parts) {
    if (current == null || typeof current !== 'object') return undefined;
    current = (current as Record<string, unknown>)[p];
  }
  return current;
}

/** UI language for display: cn/tw when selected, else en (covers jp/kr until we add those). */
export function getUiLang(language: string): UiLang {
  if (language === 'cn' || language === 'tw') return language;
  return 'en';
}

export function getUiString(lang: UiLang, key: string): string {
  const val = getNested(ui[lang], key);
  if (typeof val === 'string') return val;
  return (getNested(ui.en, key) as string) || key;
}

/** Replace {count} and {total} (etc.) in template with values. */
export function interpolate(template: string, vars: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? ''));
}
